import re
from collections import OrderedDict


def remove_duplicate_sentences1(text):
    # Normalize whitespace and split the text into sentences using a regular expression
    sentences = re.split(r'(?<=[.!?])\s+', text.strip())

    # Use an OrderedDict to remove duplicates while preserving order
    unique_sentences = list(OrderedDict.fromkeys(sentence.lower().replace('\n', ' ') for sentence in sentences))

    # Join the unique sentences back into a single string
    return '\n'.join(unique_sentences)


